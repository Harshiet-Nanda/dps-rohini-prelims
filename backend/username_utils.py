import random

def random_name():
    have_username = False
    name = ''
    while not have_username:
        random_adj = random.sample(adjs, 1)[0]
        username_adj = random.sample(usernames, 1)[0]
        random_num = random.randint(0, 50)
        name = f'{random_adj.title()}{username_adj.title()}{random_num}'
        have_username = True
    return name